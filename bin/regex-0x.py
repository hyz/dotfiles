#!/usr/bin/env python3

import sys, re

test_str = '''static uint8_t LIB_NAME[55]={
0x56, 0x04, 0x85, 0xc6, 0x56, 0xdf, 0x82, 0x49, 0x43, 0x8e, 0xc7, 0x3b, 0x5c, 0x82, 0xc6, 0x00,
0xdc, 0x57, 0xa3, 0x86, 0x39, 0x82, 0x09, 0xc1, 0x21, 0x5b, 0x43, 0xcd, 0x48, 0xc6, 0x6f, 0x45,
0x10, 0x02, 0xcb, 0x8a, 0x41, 0x55, 0xf0, 0x56, 0x82, 0x85, 0x47, 0x53, 0x8a, 0xad, 0x86, 0x0f,
0xc0, 0x58, 0xbc, 0xc0, 0x2f, 0x83, 0x27, };
'''

def convert(txt):
    txt = ''.join( x.lower() for x in txt.split() )
    txt = re.search(re.compile('\{([^\{\}]+)[^0-9]*\}',re.MULTILINE), txt).group(1)
    txt = re.sub(re.compile('0[xX](.+?)[,$]'), r'\1', txt)
    #txt = re.sub(re.compile('0[xX](.+?)[,$]'), lambda g:g.group(1), txt)
    return txt

print( convert(test_str) ) ; sys.exit()

if len(sys.argv) > 1:
    print( convert(open(sys.argv[1]).read()) )
else:
    print( convert(sys.stdin.read()) )

